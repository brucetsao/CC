<p>國立暨南國際大學，簡稱暨南大學或暨大， <br>
是一所位在中華民國臺灣省南投縣埔里鎮的國立大學。</p>
<p>&nbsp;</p>
<p><br>
  前身為清政府在1906年創立於江寧府的「暨南學堂」，<br>
  並在創立初期即以華僑為主要招生對象。<br>
  1995年，該校以在臺復校名義在臺灣省南投縣埔里鎮建校，<br>
  並陸續設立人文、管理、科技、教育等四個學院
  
  
